										<p>The Criminal Law and Judicial Advisory Service (CLJAS) of DPKO's Office for Rule of Law and Security Institutions proposes an online training programme for its officers serving in judicial and rule of law functions in United Nations peace operations. The content of this training has been delivered twice yearly in-person since 2010, in partnership with the German Center for International Peace Operations ZIF. 
										It is now available online as a predeployment training or as a refresher course. </p>
										<p>The training programme will consist of 20 modules, which are tailored to the particular training needs of judicial affairs officers.  Training methodologies will include presentations, audio explanations, videos, case studies and self knowledge evaluations. Based on the in-person version of the training programme, this online version was developed and is delivered with a focus on imparting practical knowledge, and strengthening the capacity of judicial affairs officers in peace operations to provide technical advice and support the strengthening of the rule of law. </p> 
									
