		<div id="monday" class="accordion-body collapse">
			<section class="accordion-inner">
				<p>Un spécialiste des affaires judiciaires, en poste depuis quatre mois dans le pays hôte, s’entretient avec le doyen d’une faculté de droit. Dès son arrivée, il a rencontré le doyen pour offrir son aide à la faculté dans le cadre de deux projets. Premièrement, il a proposé son assistance pour élaborer un cours consacré aux droits de l’homme, destiné à la fois aux étudiants et aux praticiens du droit. Deuxièmement, il a suggéré de créer une association d’étudiants en droit de la faculté que viendrait appuyer la création d’une association de jeunes professionnels dans tout le pays.</p>
                <p>Bien que le doyen ait de prime abord fait preuve d’intérêt et qu’il ait affirmé avoir discuté des projets
avec le conseil d’administration de la faculté de droit (composé de hauts fonctionnaires gouvernementaux et
d’éminents juristes non gouvernementaux), rien n’a été fait. Le spécialiste des affaires judiciaires a rencontré
le doyen à trois reprises, ce dernier se contentant à chaque fois de demander des fonds pour financer un
déplacement à Paris ou Londres.</p>
                <p>
                Trois mois ont passé depuis leur dernière rencontre.
                </p>

			
