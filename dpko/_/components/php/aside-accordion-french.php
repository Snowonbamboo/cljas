			<section class="accordion-inner">
			<li>Il/elle souhaite voyager ou obtenir d’autres avantages, mais se désintéresse totalement du reste;</li>
			<li>
				Il/elle s’inquiète du manque de compétences de son institution et de ses collègues et espère écarter les risques d’embarras qui pèsent sur son institution ou mettent à mal sa propre crédibilité;
			</li>
	        <li>Il/elle se sent menacé(e) par des étrangers qui peuvent mieux connaître le domaine du droit que lui/elle ou sa faculté et souhaite sauver les apparences;</li>
	        <li>
	        	Il/elle doute de la crédibilité du spécialiste des affaires judiciaires en raison de son âge, de son sexe,ce, de son ethnicité, de son expérience, de ses connaissances ou de ses qualifications (réels ou perçus);
	        </li>
			<li>Il/elle ne souhaite pas être aidé(e) par fierté ou gêne, par exemple de sa propre méconnaissance du droit;</li>
			<li> Il/elle est quelque peu agacé(e) par l’attitude du spécialiste des affaires judiciaires, qu’elle soit réelle ou perçue (ou par ouï-dire);
			</li>
			<li> 
			Il/elle a des raisons financières ou autres qui l’incitent à maintenir le statu quo [et dans des cas extrêmes, il/elle est peut-être corrompu(e)];
			</li>
				
