									<h3><span class="glyphicon glyphicon-file" aria-hidden="true"></span>  Compétences diplomatiques</h3>
								</div>
								<div class="row">
									<div class="col-xs-8 col-sm-8 col-md-8 col-lg-8">
										<p>Par compétences diplomatiques, nous faisons référence aux compétences requises pour communiquer avec les interlocuteurs nationaux. Il s’agit des compétences permettant aux spécialistes des affaires judiciaires de gagner la confiance des homologues nationaux et de travailler avec eux de manière efficace. </p>
										<p>Les compétences diplomatiques sont nécessaires pour les activités de plaidoyer, de coordination, l’institution de partenariats et la conduite des évaluations, ainsi que pour faciliter le dialogue, obtenir l’adhésion aux politiques et soutenir les partisans des réformes.</p>
										<p><strong>
											<span class="glyphicon glyphicon-list-alt" aria-hidden="true"></span>
											Principes clés en matière de compétences diplomatiques</strong></p>
										<ol>
											<li>Veiller à l’appropriation et à la direction nationales</li>
											<li>Développer les connaissances et les sensibilités locales</li>
											<li>Donner aux acteurs nationaux les moyens d’agir</li>
											<li>Aborder les besoins en matière d’état de droit au niveau politique</li>
											<li>Gérer les attentes</li>
										</ol>
